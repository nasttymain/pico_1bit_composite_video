extern constexpr const uint16_t CCOLUMNS = 64;

// ascii文字を1つ描画
void c_putc(char c){
    if(c < 32 || c == 127){
        if(c == 13){
            // CR
            cpos = cpos - (cpos % CCOLUMNS);
        }else if(c == 10){
            // LF
            cpos += CCOLUMNS;
        }else if(c == 9){
            // HT
            cpos = cpos - (cpos % 4) + 4;
        }
        return;
    }
    const uint8_t code = (uint8_t)(c) - 32;
    const int16_t base_x = (cpos % CCOLUMNS) * 8;
    const int16_t base_y = (cpos / CCOLUMNS) * 8;

    for(int16_t x = 0; x < 8; x++){
        const uint8_t row = font[code * 8 + x];
        for(int16_t y = 0; y < 8; y++){
            if(row & static_cast<uint8_t>(0x01 << y)){
                pset(base_x + x, base_y + y);
            }
        }
    }

    cpos += 1;

    return;
}

